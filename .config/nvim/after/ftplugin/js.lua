vim.opt_local.tabstop = 2 -- size of a hard tabstop (ts).
vim.opt_local.shiftwidth = 2 -- size of an indentation (sw).
vim.opt_local.expandtab = true -- always uses spaces instead of tab characters (et).
vim.opt_local.softtabstop = 2 -- number of spaces a <Tab> counts for. When 0, feature is off (sts).
